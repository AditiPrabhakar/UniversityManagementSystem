    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        if(tfNumOfDays.getText().isEmpty())
        {
            lbError.setText("Kindly enter valid information to proceed.");
        }
        else{
            lbError.setText("");
            Integer i = cbFaculty.getSelectedIndex();
            int selectedId = idsList.get(i);

            String sql = "select * from faculty where id = ?";
            
            try {
                Connection conn = ConnectionUtil.getInstance();
                PreparedStatement  pst = conn.prepareStatement(sql);
                pst.setInt(1, selectedId);
                ResultSet rs = pst.executeQuery();
                
                if(rs.next())
                {
                    int salary = rs.getInt("salary");
                    int days = Integer.parseInt(tfNumOfDays.getText());
                    int month;
                    int cbM = cbMonth.getSelectedIndex();
//                    String monthSelected = (String) cbMonth.getSelectedItem();
                    
                    month = switch (cbM) {
                        case 0, 2, 4, 6, 7, 9, 11 -> 31;
                        case 1 -> 28;
                        default -> 30;
                    };
                    double perDaySalary = salary / month;
                    double calculatedSalary = perDaySalary * days;
                    if(! (tfApplyBonus.getText().isEmpty()))
                    {                   
                        float bonus = Integer.parseInt(tfApplyBonus.getText());
//                        pst1.setString(4, String.valueOf(bonus));
//                        String bType = (String) cbBonusType.getSelectedItem();
//                        pst1.setString(5, bType);
                        calculatedSalary = calculatedSalary + (calculatedSalary * (bonus / 100));
                    }
//                    pst1.setString(3, String.valueOf(calculatedSalary));
                    if(! (tfRemarks.getText().isEmpty()))
                    {
//                        pst1.setString(6, tfRemarks.getText());
                    }
                    DecimalFormat df = new DecimalFormat("#.00");
                    String formattedSalary = df.format(calculatedSalary);
                    lbSalary.setText("Rs. "+ formattedSalary);
                }
                else
                {
                    lbError.setText("Error occured, please try again.");
                }
            }
            catch (SQLException ex){
                  Logger.getLogger(SalaryCalculate.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

