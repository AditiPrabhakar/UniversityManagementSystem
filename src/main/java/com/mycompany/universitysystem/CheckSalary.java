    private void fetchSalaryDetails()
    {
        String sql = "select * from calculated_salary where faculty_id = ? AND month = ?";
        
        String month = (String) cbMonth.getSelectedItem();
        try {
            Connection conn = ConnectionUtil.getInstance();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, id);
            pst.setString(2, month);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next())
            {
                String salary = rs.getString("salary");
                String numOfDays = rs.getString("days_worked");
                String bonus = rs.getString("bonus");
                String bonus_type = rs.getString("bonus_type");
                String remarks = rs.getString("remarks");
                String status = rs.getString("status");
                
                lbNumOfDays.setText(numOfDays);
                lbSalary.setText(salary);
                lbBonus.setText(bonus + "%");
                lbBonusType.setText(bonus_type);
                lbRemarks.setText(remarks);
                
                if(status != null)
                {
                    if(status.equalsIgnoreCase("Paid"))
                    {
                        lbError.setText("This month salary has been paid.");
                    }
                }  
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(CheckSalary.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
