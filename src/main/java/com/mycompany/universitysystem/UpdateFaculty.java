    private void cbFacultyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbFacultyMouseClicked
        // TODO add your handling code here:
        lbError.setText("");
        Integer i = cbFaculty.getSelectedIndex();
        int selectedId = idsList.get(i);
        String selectedName = nameList.get(i);
        String id = String.valueOf(selectedId);
        lbName.setText(selectedName);
        lbEmployeeId.setText(id);
        
        String sql = "select * from faculty where id = ?";
            
        Connection conn;
        try {
            conn = ConnectionUtil.getInstance();
            PreparedStatement  pst = conn.prepareStatement(sql);
            pst.setInt(1, selectedId);
            ResultSet rs = pst.executeQuery();
                
            if(rs.next())
            {
                String salary = rs.getString("salary");
//                String name = rs.getString("name");
                String fname = rs.getString("fname");
                String mname = rs.getString("mname");
                String dob = rs.getString("dob");
                String address = rs.getString("address");
                String email = rs.getString("email");
                String password = rs.getString("password");
                String education = rs.getString("education");
                String department = rs.getString("department");
                String role = rs.getString("role");   
                String expertise = rs.getString("expertise");
                
                cbEducation.setSelectedItem(education);
                tfFname.setText(fname);
                tfMname.setText(mname);
                tfDob.setText(dob);
                tfAddress.setText(address);
                tfEmail.setText(email);
                tfPassword.setText(password);
                tfDepartment.setText(department);
                cbRole.setSelectedItem(role);
                tfSalary.setText(salary);
                tfExpertise.setText(expertise);
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateFaculty.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_cbFacultyMouseClicked

