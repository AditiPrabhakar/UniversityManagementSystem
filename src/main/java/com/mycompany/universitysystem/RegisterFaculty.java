    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(tfEmail.getText().isEmpty() || tfName.getText().isEmpty() )
        {
            lbError.setText("All fields are required.");
        }
        else{
            lbError.setText("");
            String name = tfName.getText();
            String email = tfEmail.getText();
            String password = tfPassword.getText();
            String salary = tfSalary.getText();
            String expertise = tfExpertise.getText();
            String education = (String) cbEducation.getSelectedItem();
            String fname = tfFname.getText();
            String mname = tfMname.getText();
            String department = tfDepartment.getText();
            String role = (String) cbRole.getSelectedItem();
            String address = tfAddress.getText();
            String dob = tfDob.getText();
                    
            String sql = "INSERT INTO faculty (name,email, password, salary, expertise, education, fname, mname, department, role, address, dob) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            try {
                Connection conn = ConnectionUtil.getInstance();
                PreparedStatement  pst = conn.prepareStatement(sql);
                pst.setString(1, name);
                pst.setString(2, email);
                pst.setString(3, password);
                pst.setString(4, salary);
                pst.setString(5, expertise);
                pst.setString(6, education);
                pst.setString(7, fname);
                pst.setString(8, mname);
                pst.setString(9, department);
                pst.setString(10, role);
                pst.setString(11, address);
                pst.setString(12, dob);
                int a = pst.executeUpdate();

                if(a > 0)
                {
                    lbError.setText("Successfully registered!");
                }
                else
                {
                    lbError.setText("Error in registeration, please try again.");
                }
            }
            catch (SQLException ex) {
                Logger.getLogger(StudentRegister.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnSubmitActionPerformed
    }
